namespace UWP.Toolkit.Controls.Enum;

/// <summary>
/// Enum: Alignment of the Toast.
/// </summary>
public enum ToastAlignment
{
    /// <summary>
    /// Position the toast at the top left corner of the screen.
    /// </summary>
    TopLeft,
    /// <summary>
    /// Position the toast at the top center of the screen.
    /// </summary>
    TopCenter,
    /// <summary>
    /// Position the toast at the top right corner of the screen.
    /// </summary>
    TopRight,
    /// <summary>
    /// Position the toast at the bottom left corner of the screen.
    /// </summary>
    BottomLeft,
    /// <summary>
    /// Position the toast at the bottom center of the screen.
    /// </summary>
    BottomCenter,
    /// <summary>
    /// Position the toast at the bottom right corner of the screen.
    /// </summary>
    BottomRight
}
